    function br2nl($string){
    //---------------------------------------------------
    //br2nl
    //---------------------------------------------------

        $nl="\r\n";
        $replace=array(
            "</BR>"=>$nl,
            "</br>"=>$nl,
            "</Br>"=>$nl,
            "</bR>"=>$nl,
            "<BR>" =>$nl,
            "<br>" =>$nl,
            "<Br>" =>$nl,
            "<bR>" =>$nl
        );
        return strtr($string,$replace);
    }
?>
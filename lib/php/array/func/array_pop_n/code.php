<?php
//-------------------------------------------------------
//函式: array_pop_n()
//用途: 從後頭刪除N個元素
//日期: 2011年12月1日
//作者: jeff@max-life
//-------------------------------------------------------

    function array_pop_n(&$array,$N){
    //---------------------------------------------------
    //從後頭刪除N個元素
    //---------------------------------------------------
    //$arr  陣列
    //$N    個數
    //
    //本函式回傳被移除的元素,型態為陣列
    //---------------------------------------------------

        $R=array();

        if($N>=count($array)){
            $N=count($array);
        }

        for($i=1;$i<=$N;$i++){
            $R[]=array_pop($array);
        }

        return $R;
    }
?>
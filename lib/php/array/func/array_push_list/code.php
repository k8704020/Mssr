<?php
//-------------------------------------------------------
//函式: array_push_list()
//用途: 串列文字附加至陣列
//日期: 2011年12月1日
//作者: jeff@max-life
//-------------------------------------------------------

    function array_push_list($arr,$list,$dc=","){
    //---------------------------------------------------
    //串列文字附加至陣列
    //---------------------------------------------------
    //$arr  陣列
    //$list 串列文字
    //$dc   分隔符號
    //
    //本函式回傳陣列
    //---------------------------------------------------
        return array_merge($arr,explode($dc,$list));
    }
?>
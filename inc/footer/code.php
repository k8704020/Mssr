    function footer($rd=0){
    //---------------------------------------------------
    //函式: footer()
    //用途: 註腳列
    //---------------------------------------------------
    //$rd   層級指標,預設0,表示在目前目錄下
    //---------------------------------------------------

        //-----------------------------------------------
        //參數檢驗
        //-----------------------------------------------

            if(!isset($rd)){
                $rd='';
            }else{
                $rd=str_repeat('../',$rd);
            }

        //-----------------------------------------------
        //設定
        //-----------------------------------------------

            //-------------------------------------------
            //key       代碼
            //cname     文字
            //url       路徑
            //target    框架    _blank | _self | 視窗id
            //state     狀態    on:顯示 | off:隱藏
            //-------------------------------------------

                $date=date("Y",time());
                $corp="National Central University All Rights Reserved.";
                $items=array(
                    "copyright"=>array(
                        "key"   =>"copyright",
                        "cname" =>"國立中央大學 版權所有 &copy; 2008-{$date} {$corp}",
                        "url"   =>"#",
                        "target"=>"_self",
                        "state" =>"on"
                    )
                );

        //-----------------------------------------------
        //處理
        //-----------------------------------------------

            $items_tmp=array();
            foreach($items as $key=>$item){
                if($item['state']=='on'){
                    $items_tmp[$key]=$item;
                }
            }

            if(1==2){ //除錯用
                echo "<pre>";
                print_r($items_tmp);
                echo "</pre>";
            }

        //-----------------------------------------------
        //回傳
        //-----------------------------------------------

            return $items_tmp;
    }
?>
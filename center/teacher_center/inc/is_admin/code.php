<?php
//-------------------------------------------------------
//函式: is_admin()
//用途: 是否為管理者
//-------------------------------------------------------

    function is_admin($permission){
    //---------------------------------------------------
    //函式: is_admin()
    //用途: 是否為管理者
    //---------------------------------------------------
    //permission    身分
    //---------------------------------------------------

        //-----------------------------------------------
        //預設值
        //-----------------------------------------------

            $flag=false;

        //-----------------------------------------------
        //參數檢驗
        //-----------------------------------------------

            //接收參數
            if(isset($permission)&&(trim($permission)!=='')){
                $permission=trim($permission);
                if($permission==='super'){
                    $flag=true;
                }
            }

        //-----------------------------------------------
        //回傳
        //-----------------------------------------------

            return $flag;
    }
?>